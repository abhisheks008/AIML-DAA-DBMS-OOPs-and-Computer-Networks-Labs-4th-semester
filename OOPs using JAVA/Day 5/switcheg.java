// Give an example of Switch case statement in JAVA Program.
public class switcheg {
	public static void main (String[] args){
		char ch = 'o';
		switch (ch){
			case 'a':
			System.out.println ("Vowel");
			break;

			case 'e':
			System.out.println ("Vowel");
			break;

			case 'i':
			System.out.println ("Vowel");
			break;

			case 'o':
			System.out.println ("Vowel");
			break;

			case 'u':
			System.out.println ("Vowel");
			break;

			default:
			System.out.println ("Consonant");
			break;
		}
	}
}


// Output :
// Vowel
